package FirstP;

public class Calculadora4Operacoes {
    public Calculadora4Operacoes(){

    }


    //Metodos
    public Double soma(Double op1, Double op2 ){
        return op1 + op2;
    }

    public Double subtracao(Double op1, Double op2){
        return op1 - op2;
    }

    public Double multiplic(Double op1, Double op2){
        return op1*op2;
    }

    public Double divisao(Double op1, Double op2){
        return op1 / op2;

    }

}
